// const keyword is used to create a constant variable.
// it can not change its value once it defined.

// example
const username = "kunalsk36";
console.log(username);

// if we try to change it will throw error
// username = "newUserName";
// console.error(username); if we run these these will throw an error

// we can change values of variables which is defined by var or let.
let myFriendName = "Kunal";
console.log(myFriendName);
myFriendName = "Tanvi";
console.log(myFriendName);
